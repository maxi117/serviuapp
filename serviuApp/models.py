from django.db import models

class Subsidio(models.Model):
    id = models.AutoField(primary_key=True)
    decreto = models.CharField(max_length=100)
    ano_ejecucion = models.IntegerField()
    mes_pago = models.CharField(max_length=100)
    ano_programa = models.IntegerField()
    tipo_programa = models.CharField(max_length=250)
    alternativa = models.CharField(max_length=250)
    tipo_proyecto = models.CharField(max_length=250)
    incremento = models.CharField(max_length=100)
    region = models.CharField(max_length=100)
    provincia = models.CharField(max_length=100)
    comuna = models.CharField(max_length=100)
    codigo_rut_proyecto_grupo_beneficiario = models.CharField(max_length=100)
    nombre_proyecto_beneficiario = models.CharField(max_length=250)
    sistema_utilizado = models.CharField(max_length=250)
    resolucion_asignacion_subsidios = models.CharField(max_length=250)
    fecha = models.DateField()
    unidad = models.CharField(max_length=100)
    uf = models.IntegerField()
    valor_uf_utilizado = models.IntegerField()
    valor = models.IntegerField()
    valor_miles = models.IntegerField()
    observacion = models.CharField(max_length=500)
    num_resolucion_pago = models.IntegerField()
    fecha_resolucion_pago = models.DateField()
    num_ap_rpm = models.CharField(max_length=100)
    fecha_ap_rpm = models.DateField()
    rut_beneficiario = models.CharField(max_length=100)
    dv = models.CharField(max_length=100)
    num_giro = models.IntegerField()
    porcentaje_avance_cuota = models.IntegerField()
    ahorro = models.IntegerField()
    nombre_constructora_vendedor = models.CharField(max_length=250)
    rut_constructora_vendedor = models.CharField(max_length=300)
    dv_constructora_vendedor = models.CharField(max_length=300)
    nombre_mandante = models.CharField(max_length=250)
    rut_mandante = models.CharField(max_length=100)
    dv_mandante = models.CharField(max_length=100)
    ejecucion_pago = models.CharField(max_length=100)
    num_ingreso_ofpa = models.IntegerField()
    fecha_ingreso_ppeps = models.DateField()
    caja_semana = models.CharField(max_length=100)
